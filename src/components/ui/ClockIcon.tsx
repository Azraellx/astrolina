// A small clock face (dial + hour/minute hands) for time-of-day labels — e.g.
// the sky band's local-time readout. Strokes in currentColor so each host
// colours it; sized like the sibling EyeIcon.
export function ClockIcon({
  className,
  size = 13,
}: {
  className?: string;
  size?: number;
}) {
  return (
    <svg
      className={className}
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      aria-hidden="true"
    >
      <circle cx="12" cy="12" r="9" />
      <path d="M12 7v5l3.5 2" />
    </svg>
  );
}
